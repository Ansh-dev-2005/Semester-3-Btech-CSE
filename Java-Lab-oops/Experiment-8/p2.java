public class p2 {

    public static void main(String[] args) {
        
        String s1 = new String("String1");
        StringBuffer s2 = new StringBuffer("String2");

        String s3 = "String3";
        // StringBuffer s4 = "String4";


        System.out.println("String1: " + s1);
        System.out.println("String2: " + s2);
        System.out.println("Primitive way of initializing string: ");
        System.out.println("String3: " + s3);
        // System.out.println("String4: " + s4);
        System.out.println("Using toLowerCase() method: ");
        System.out.println("String1: " + s1.toLowerCase());
        // System.out.println("String2: " + s2.toLowerCase());
        System.out.println("String3: " + s3.toLowerCase());
        // System.out.println("String4: " + s4.toLowerCase());




    }


    
}
